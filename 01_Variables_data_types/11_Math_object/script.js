// For Square root 
let x;
x = Math.sqrt(49);
console.log(x);

// For absolute Number 
let a = Math.abs(-5);
console.log(a);

// For Round off
let b = Math.round(1.5);
console.log(b);

// Round up  
let c = Math.ceil(1.1);
console.log(c);

// Round off less
let d = Math.floor(1.8);
console.log(d);

// Power
let e = Math.pow(2,2);
console.log(e);

// Min and Max
let f = Math.min(4,5,6,1);
console.log(f);
let g = Math.max(1,4,7,8,9);
console.log(g);


